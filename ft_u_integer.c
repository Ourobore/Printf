#include "ft_printf.h"

void	ft_unsigned_integer(t_flags f, t_type t)
{
	if (t.u < 0)
		t.i = 4294967296 + t.u;
	else
		t.i = t.u;
	ft_integer(f, t, "0123456789");
}
